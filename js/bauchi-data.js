// ===== HELPER FUNCTIONS =====
export function getAllLGAs() {
  return BAUCHI_DATA.lgAs.map(lga => ({
    id: lga.id,
    name: lga.name,
    code: lga.code,
    headquarters: lga.headquarters
  }));
}

export function getWardsByLGA(lgaName) {
  const lga = BAUCHI_DATA.lgAs.find(l => l.name === lgaName);
  return lga ? lga.wards.map(w => w.name) : [];
}

export function getPollingUnitsByWard(lgaName, wardName) {
  const lga = BAUCHI_DATA.lgAs.find(l => l.name === lgaName);
  if (!lga) return [];
  const ward = lga.wards.find(w => w.name === wardName);
  return ward ? ward.pollingUnits : [];
}

export function getAllPollingUnitsByLGA(lgaName) {
  const lga = BAUCHI_DATA.lgAs.find(l => l.name === lgaName);
  if (!lga) return [];
  let allPUs = [];
  lga.wards.forEach(ward => {
    allPUs = allPUs.concat(ward.pollingUnits);
  });
  return allPUs;
}

export function getTotalCounts() {
  let totalWards = 0;
  let totalPUs = 0;
  
  BAUCHI_DATA.lgAs.forEach(lga => {
    totalWards += lga.wards.length;
    lga.wards.forEach(ward => {
      totalPUs += ward.pollingUnits.length;
    });
  });
  
  return {
    lgAs: BAUCHI_DATA.lgAs.length,
    wards: totalWards,
    pollingUnits: totalPUs
  };
}
